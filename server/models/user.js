import mongoose from "mongoose";
import handleMogooseError from "../helpers/handleMogooseError.js";

const UserSchema = new mongoose.Schema(
  {
    firstName: {
      type: String,
      required: true,
      min: 2,
      max: 50,
    },
    lastName: {
      type: String,
      required: true,
      min: 2,
      max: 50,
    },
    email: {
      type: String,
      required: true,
      max: 50,
      unique: true,
    },
    password: {
      type: String,
      required: true,
      min: 5,
    },
    picturePath: {
      type: String,
      default: "",
    },
    friends: {
      type: Array,
      default: [],
    },
    location: String,
    occupation: String,
    viewedProfile: Number,
    impressions: Number,
  },
  { versionKey: false, timestamps: true }
);
mongoose.models = {};
UserSchema.post("save", handleMogooseError);
const User = mongoose.model("User", UserSchema);
export default User;
